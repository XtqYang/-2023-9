def guiji_hash(data, id_name='time'):
    out = []
    i = data[0][id_name]
    for j in range(len(data)):
        if data[j][id_name] > i + 33.3333333333333 * (len(out) + 1):
            out.append(data[j - 1])
    out.append(data[-1])
    return out


